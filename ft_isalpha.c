#include "libft.h"

int	ft_isalpha(int i)
{
	while (i != '\0')
	{
		if (i >= 'a' && i <= 'b')
			return (1);
		else if (i >= 'A' && i <= 'B')
			return (1);
		else
			return (0);
	}
}
